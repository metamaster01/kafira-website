//  AUTO-COMPUTE startingPrice & tripCount on every Destination
//  This runs once at module load — keeps data in sync automatically
// ─────────────────────────────────────────────────────────────────
DESTINATIONS.forEach((dest) => {
  const destTrips = TRIPS.filter((t) => t.destinationSlug === dest.slug);
  dest.tripCount = destTrips.length;
  dest.startingPrice =
    destTrips.length > 0
      ? Math.min(...destTrips.map((t) => t.discountedPrice))
      : 0;
});

